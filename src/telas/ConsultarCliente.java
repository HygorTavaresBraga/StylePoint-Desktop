    void consultar() {
        try {
            String cpf = jcpf.getText();
            ClienteDao dao = new ClienteDao();
            Cliente cliente = dao.consultar(cpf);
            if (cliente == null) {
                JOptionPane.showMessageDialog(null, "Cliente nao Encontrado");
                limpar();
            }else{
                jcodigo.setText(String.valueOf(cliente.getIdCliente()));
                jnome.setText(cliente.getNomeCliente());
                jdataNasc.setText(cliente.getDataNascimentoCliente());
                jtelefone.setText(cliente.getTelefoneCliente());
                jemail.setText(cliente.getEmailCliente());
                jendereco.setText(cliente.getEnderecoCliente());
                jeditar.setEnabled(true);
                jsalvar.setEnabled(true);
                jexcluir.setEnabled(true);
                JOptionPane.showMessageDialog(null,"Cliente encontrado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "erro no SQL" + e.getMessage());
        }
    }
    void salvar(){
        String cpf = jcpf.getText();
        String nome = jnome.getText();
        String data = jdataNasc.getText();
        String telefone = jtelefone.getText();
        String email = jemail.getText();
        String endereco = jendereco.getText();
        
        Cliente cliente = new Cliente();
        
        cliente.setCpfCliente(cpf);
        cliente.setNomeCliente(nome);
        cliente.setDataNascimentoCliente(data);
        cliente.setTelefoneCliente(telefone);
        cliente.setEmailCliente(email);
        cliente.setEnderecoCliente(endereco);
        
        ClienteDao dao = new ClienteDao();
        dao.atualizar(cliente);
        JOptionPane.showMessageDialog(null, "Cliente Atualizado");
        limpar();
        
    }
    
