   void cadastrar(){
        try{
            //Instância do Locação;
            LocacaoDao dao = new LocacaoDao();
            Locacao l = new Locacao();
            
            //Instância de Cliente e Estoque;
            ClienteDao daoC = new ClienteDao();   
            Cliente c = new Cliente();
            
            EstoqueDao daoE = new EstoqueDao();   
            Estoque e = new Estoque();
            
            
            //1ºCria variável e atribui o item selecionado da JComboBox;
            //2ºInsere o nome atribuido na variável dentro de Cliente;
            //3ºUtiliza o método criado no DAO;
            
            String cli = (String)jcliente.getSelectedItem();
            c.setNomeCliente(cli);            
            daoC.buscanome(c);
            
            String roupa = (String)jroupa.getSelectedItem();
            e.setNomeRoupa(roupa);
            daoE.buscanomeR(e);
            
            /*Teste de Recebimento de ID's;
            
            JOptionPane.showMessageDialog(null, c.getIdCliente());
            JOptionPane.showMessageDialog(null, c.getNomeCliente());
            JOptionPane.showMessageDialog(null, e.getIdRoupa());
            JOptionPane.showMessageDialog(null, e.getNomeRoupa());*/

            
            //CADASTRO;
            l.setIdCliente(c.getIdCliente());
            l.setIdRoupa(e.getIdRoupa());
            l.setDataLocacao(jdataLocacao.getText());
            l.setDataDevolucao(jdataDevolucao.getText());
            l.setObservacao(jobservacao.getText());
            l.setValorTotal(Double.parseDouble(jvalor.getText()));
            
            dao.adicionar(l);
            JOptionPane.showMessageDialog(null,"Locação Cadastrada!");
            limpar();
            
        }catch(Exception e){JOptionPane.showMessageDialog(null,"Erro ao cadastrar Locação "+e.getMessage());
    }
    }
    

    
   
