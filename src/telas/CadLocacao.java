    private void preenche_combo(){      
        Cliente c = new Cliente();
        ClienteDao daoC = new ClienteDao();
        List<Cliente> cliente = daoC.PreencheCliente();
       
        for(Cliente cli: cliente){
           
        jcliente.addItem(cli.getNomeCliente());
           
        }
        
        Estoque e = new Estoque();
        EstoqueDao daoR = new EstoqueDao();
        List<Estoque> estoque = daoR.PreencheEstoque();
       
        for(Estoque rou: estoque){
           
        jroupa.addItem(rou.getNomeRoupa());
           
        }
     
     }

    
